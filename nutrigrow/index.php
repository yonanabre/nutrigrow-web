      <p><a style="color:red"><b>PT. BERKAT MITRA MAKMUR </b></a> atau disingkat dengan sebutan <a style="color:blue"><b>"BMM Group"</b></a> merupakan pengembangan dari induk perusahaan bernama: CV. Berkat Manna Maju yang berkedudukan di Surakarta, Jawa Tengah, Indonesia didirikan pada tanggal 10 April 2005 silam yang awalnya pembetukan perusahaan tersebut dengan cara bergabung secara manajemen dan support dengan perusahaan SOLIBERTEC SL yang berkedudukan di Madrid, Spain, sebagai perusahaan produsen, distributor, importir, eksportir dan trading untuk dalam dan luar negeri. </p>

      <p> Awal perusahaan kami mendapatkan izin edar pada Agustus 2006 dari <a style="color:green"><b>DEPTAN R.I dengan merek dagang NUTRIGROW®</b></a> yaitu produk pupuk cair Makro dan Mikro berbasis bahan baku organik dengan kapasitas produksi 75 liter/minggu. Setelah 3 bulan berjalan dan respon positif pasar untuk produk Nutrigrow® berdampak kepada kapasitas harus dinaikan menjadi 100-125 liter/minggu dan seterusnya hingga 1 (satu) tahun berjalan dan menambah kapasitas menjadi 350 liter di bulan Agustus 2007. </p>

      <span class="divider"><i class="fas fa-leaf"></i></span>

      <p>Perjalanan bisnis ini tidak mudah walau pada saat itu pesaing dari perusahaan lain masih sangat minim tetapi untuk mengubah edukasi (pemahaman) petani di Indonesia dalam menggunakan produk organic sangat tidak mudah, berjalannya waktu hingga Januari 2015 produksi kami sudah mencapai 5.500 liter/bulannya dan kami juga menambah produk baru dan mendapat izin edar dari <a style="color:green"><b>DEPTAN R.I dengan merek dagang FITGROW®</b></a> yang berorientasi kepada produk hormone dan pembobot buah. Pada Agustus 2019 kapasitas produksi produk Nutrigrow® sudah mencapai 27.000 - 30.000 liter/bulan dan Fitgrow® 10.000 - 15.000 liter/bulan hingga saat ini.</p>

